package com.dhbw.hangman.model;

import jakarta.persistence.Embeddable;

@Embeddable
public class Player {

    private String name;
    private String role;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
